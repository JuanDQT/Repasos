public class Persona {

    private String name = "a";
    private Integer suerte;

    public Persona() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getSuerte() {
        return suerte;
    }

    public void setSuerte(Integer suerte) {
        this.suerte = suerte;
    }
}
